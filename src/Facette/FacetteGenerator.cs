using System;
using Facette.Generator.Builders;
using Facette.Generator.Diagnostics;
using Facette.Generator.Models;
using Microsoft.CodeAnalysis;

namespace Facette.Generator
{
    [Generator]
    public sealed class FacetteGenerator : IIncrementalGenerator
    {
        internal const string AutoGeneratedHeader = "// <auto-generated />\n#nullable enable\n";
        private const string HookOnAfterFromSource = "partial void OnAfterFromSource";
        private const string HookOnBeforeToSource = "partial void OnBeforeToSource";
        private const string HookOnAfterToSource = "partial void OnAfterToSource";

        public void Initialize(IncrementalGeneratorInitializationContext context)
        {
            var targets = context.SyntaxProvider.ForAttributeWithMetadataName(
                "Facette.Abstractions.FacetteAttribute",
                predicate: static (node, _) => true,
                transform: static (ctx, _) =>
                {
                    try
                    {
                        return ModelBuilder.Build(ctx);
                    }
                    catch (Exception)
                    {
                        return null;
                    }
                }
            );

            context.RegisterSourceOutput(targets, static (spc, model) =>
            {
                if (model == null)
                {
                    return;
                }

                // Report any diagnostics collected during model building
                bool hasErrors = false;
                foreach (var diag in model.Diagnostics)
                {
                    spc.ReportDiagnostic(Diagnostic.Create(
                        diag.Descriptor,
                        diag.ToLocation(),
                        diag.MessageArgs));

                    if (diag.Descriptor.DefaultSeverity == DiagnosticSeverity.Error)
                    {
                        hasErrors = true;
                    }
                }

                // Don't generate code if there are errors
                if (hasErrors)
                {
                    return;
                }

                try
                {
                    EmitDtoSource(spc, model);

                    if (model.GenerateMapper)
                    {
                        var mapperCode = MapperClassBuilder.Build(model);
                        spc.AddSource(model.TypeName + "Mapper.g.cs", mapperCode);
                    }
                }
                catch (Exception ex)
                {
                    spc.ReportDiagnostic(Diagnostic.Create(
                        DiagnosticDescriptors.FCT099_InternalError,
                        Location.None,
                        model.TypeName,
                        ex.Message));
                }
            });
        }

        private static void EmitDtoSource(SourceProductionContext spc, FacetteTargetModel model)
        {
            var properties = PropertyBuilder.Build(model.Properties, model.NullableMode);

            var fromSource = MappingBuilder.BuildFromSource(
                model.TypeName,
                model.SourceTypeFullName,
                model.Properties,
                model.HasBaseFacette,
                model.AdditionalSources);

            var mappingMethods = "\n\n" + fromSource;

            if (model.GenerateToSource)
            {
                var toSource = MappingBuilder.BuildToSource(
                    model.SourceTypeFullName,
                    model.Properties,
                    model.HasBaseFacette,
                    model.TypeName,
                    model.NullableMode);
                mappingMethods = mappingMethods + "\n\n" + toSource;
            }

            if (model.GenerateProjection)
            {
                var projection = ProjectionBuilder.Build(
                    model.TypeName,
                    model.SourceTypeFullName,
                    model.Properties,
                    model.HasBaseFacette);
                mappingMethods = mappingMethods + "\n\n" + projection;

                var expressionMapping = ExpressionMappingBuilder.Build(
                    model.TypeName,
                    model.SourceTypeFullName,
                    model.Properties,
                    model.HasBaseFacette);
                mappingMethods = mappingMethods + "\n\n" + expressionMapping;
            }

            // Check if any property is a collection — if so, we need using System.Linq
            var hasCollections = false;
            var hasImmutableCollections = false;
            foreach (var p in model.Properties)
            {
                if (p.MappingKind == MappingKind.Collection)
                {
                    hasCollections = true;
                    if (p.CollectionConvertMethod.Contains("Immutable"))
                    {
                        hasImmutableCollections = true;
                    }
                }
            }
            var linqUsing = hasCollections ? "using System.Linq;\n" : "";
            if (hasImmutableCollections)
            {
                linqUsing += "using System.Collections.Immutable;\n";
            }

            var nsLine = string.IsNullOrEmpty(model.Namespace) ? "" : "namespace " + model.Namespace + ";\n\n";

            // Build partial method hook declarations
            var hookParams = model.SourceTypeFullName + " source";
            foreach (var addSrc in model.AdditionalSources)
            {
                hookParams += ", " + addSrc.SourceTypeFullName + " " + addSrc.ParameterName;
            }
            var hooks = "\n\n    " + HookOnAfterFromSource + "(" + hookParams + ");";
            if (model.GenerateToSource)
            {
                hooks += "\n    " + HookOnBeforeToSource + "();"
                    + "\n    " + HookOnAfterToSource + "(" + model.SourceTypeFullName + " result);";
            }

            var code = AutoGeneratedHeader
                + linqUsing + "\n"
                + nsLine
                + "public partial record " + model.TypeName + "\n"
                + "{\n"
                + properties
                + mappingMethods
                + hooks + "\n"
                + "}\n";

            spc.AddSource(model.TypeName + ".g.cs", code);
        }
    }
}
